def main():
    num1 = int(input())
    num2 = int(input())
    sum = num1+num2
    difference = num1-num2
    product = num1*num2
    quotient = num1/num2
    print("Sum:%.2f"%sum)
    print("Difference:%.2f"%difference)
    print("Product:%.2f"%product)
    print("Quotient:%.2f"%quotient)


if __name__ == '__main__':
    main()